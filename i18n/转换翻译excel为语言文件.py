def read_excel(filename):

    for f in filedict.values():
        if os.path.exists(f):
            os.remove(f)

    sheet_names = pd.ExcelFile(filename).sheet_names
    for sheet_name in sheet_names:
        if sheet_name.startswith("菜单") or sheet_name.startswith("导出"):
            continue
        df = pd.read_excel(filename, sheet_name=sheet_name)

        # 获取列名
        column_names = df.columns.tolist()

        for column_name in column_names:
            if column_name == '编号' or column_name == '标签翻译后最大长度':
                continue
            else:

                # 删除包含空值的行
                df = df.dropna(subset=['编号', column_name])

                # 将数据转换为字典
                exceptions_dict = df.set_index('编号')[column_name].to_dict()

                # 将字典内容写入文本文件
                with open(filedict[column_name], "a", encoding='utf-8') as file:
                    file.write(f"// {sheet_name} \n")
                    for key, value in exceptions_dict.items():
                        if key == 'nan':
                            continue
                        value = value.replace("'", "\\'")
                        file.write(f"'{key}': '{value}',\n")

