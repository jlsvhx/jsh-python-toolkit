def generate_sql_case(text):
    lines = text.split('\n')
    sql_statements = []
    for line in lines:
        if line.strip():  # 忽略空行
            english, chinese = line.split(' = ')
            sql_statement = f"WHEN '{chinese.strip()}' THEN '{english.strip()}'"
            sql_statements.append(sql_statement)
    return "\n".join(sql_statements)

